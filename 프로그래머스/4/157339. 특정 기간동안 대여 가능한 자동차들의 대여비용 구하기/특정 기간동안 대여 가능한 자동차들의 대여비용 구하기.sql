SELECT CAR_ID, C1.CAR_TYPE, (DAILY_FEE * 30) * (100-DISCOUNT_RATE) / 100 AS FEE
FROM CAR_RENTAL_COMPANY_CAR C1 INNER JOIN CAR_RENTAL_COMPANY_DISCOUNT_PLAN P1
ON C1.CAR_TYPE = P1.CAR_TYPE AND P1.DURATION_TYPE = '30일 이상'
WHERE CAR_ID NOT IN (
    SELECT CAR_ID
    FROM CAR_RENTAL_COMPANY_RENTAL_HISTORY
    WHERE TO_CHAR(END_DATE, 'YYYY-MM-DD') > '2022-11-01'
)
AND C1.CAR_TYPE IN ('세단', 'SUV')
AND (DAILY_FEE * 30) * (100-DISCOUNT_RATE) / 100 >= 500000
AND (DAILY_FEE * 30) * (100-DISCOUNT_RATE) / 100 < 2000000
ORDER BY 3 DESC, 2, 1 DESC